#!/usr/bin/ruby

# For the string “Hello, Ruby,” find the index of the word “Ruby.”
# I assume that the comma and colon inside the quote marks is a LaTeX effect and
# that the actual strings are "Hello, Ruby." (colon, not comma) and "Ruby" (no colon).
# See http://ruby-doc.org/core-2.0/String.html#method-i-index
puts 'Hello, Ruby.'.index('Ruby')
